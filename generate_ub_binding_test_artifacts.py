def build_excel():
    wb = Workbook()
    headers = [
        "模块",
        "用例编号",
        "用例标题",
        "优先级",
        "是否阻塞",
        "首轮必测",
        "前置条件",
        "测试步骤",
        "预期结果",
        "备注",
        "关联接口/文档",
    ]
    hf = PatternFill("solid", fgColor="4472C4")
    hfont = Font(color="FFFFFF", bold=True)
    block_fill = PatternFill("solid", fgColor="FFC7CE")

    def write_sheet(ws, cases, title_color="4472C4"):
        ws.title = ws.title or "Sheet"
        fill = PatternFill("solid", fgColor=title_color)
        for col, h in enumerate(headers, 1):
            c = ws.cell(row=1, column=col, value=h)
            c.fill = fill
            c.font = hfont
            c.alignment = Alignment(wrap_text=True, vertical="center")
        for i, c in enumerate(cases, 2):
            row = list(c)
            if len(row) == 11:
                module, cid, title, pri, blocking, first, pre, steps, expect, note, api = row
                doc = api
                if "90196" in f"{note}{steps}{api}":
                    doc = (doc + "\n" if doc else "") + "https://developer.work.weixin.qq.com/document/path/90196"
                if "92228" in f"{note}{steps}{api}":
                    doc = (doc + "\n" if doc else "") + "https://developer.work.weixin.qq.com/document/path/92228"
                row = [module, cid, title, pri, blocking, first, pre, steps, expect, note, doc.strip()]
            for col, v in enumerate(row, 1):
                cell = ws.cell(row=i, column=col, value=v)
                cell.alignment = Alignment(wrap_text=True, vertical="top")
            if row[4] == "是":
                for col in range(1, len(headers) + 1):
                    ws.cell(row=i, column=col).fill = block_fill
        for col in range(1, len(headers) + 1):
            ws.column_dimensions[get_column_letter(col)].width = 16 if col not in (8, 9) else 44
        ws.freeze_panes = "A2"

    ws_meta = wb.active
    ws_meta.title = "需求追溯"
    ws_meta["A1"], ws_meta["B1"] = "功能范围", "设置—用户企微绑定管理（员工同步90196 + 绑定商城用户）"
    ws_meta["A2"], ws_meta["B2"] = "参考文档", "企微通讯录同步 https://developer.work.weixin.qq.com/document/path/90196"
    ws_meta["A3"], ws_meta["B3"] = "页面结构", "汇总(同步按钮+三卡片) | 绑定列表(筛选) | 列表列 | 操作(绑定/解绑弹窗)"
    ws_meta["A4"], ws_meta["B4"] = "首轮建议", "A权限→B同步→C统计→F/G绑定解绑→D筛选→E展示；阻塞项失败暂停"
    ws_meta["A5"], ws_meta["B5"] = "统计", (
        f"共{len(CASES)}条；阻塞{sum(1 for x in CASES if x[4]=='是')}条；"
        f"首轮{sum(1 for x in CASES if x[5]=='是')}条；P0={sum(1 for x in CASES if x[3]=='P0')}条"
    )
    ws_meta.column_dimensions["A"].width = 12
    ws_meta.column_dimensions["B"].width = 80

    ws_all = wb.create_sheet("测试用例")
    write_sheet(ws_all, CASES)

    ws_smoke = wb.create_sheet("首轮冒烟")
    write_sheet(ws_smoke, [c for c in CASES if c[5] == "是"], "548235")

    ws_block = wb.create_sheet("阻塞场景")
    bh = ["模块", "用例编号", "用例标题", "优先级", "阻塞说明", "失败影响"]
    for col, h in enumerate(bh, 1):
        c = ws_block.cell(row=1, column=col, value=h)
        c.fill = PatternFill("solid", fgColor="C00000")
        c.font = hfont
    for i, c in enumerate([x for x in CASES if x[4] == "是"], 2):
        vals = [c[0], c[1], c[2], c[3], c[9] or c[2], "员工无法正确绑定，影响好友池负责人与线索分配"]
        for col, v in enumerate(vals, 1):
            ws_block.cell(row=i, column=col, value=v).alignment = Alignment(wrap_text=True, vertical="top")
    ws_block.freeze_panes = "A2"

    wb.save(XLSX_PATH)


