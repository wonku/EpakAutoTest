def tid():
    return str(uuid.uuid4()).replace("-", "")[:16]


def add_topic(parent, title, children=None):
    node = {"id": tid(), "class": "topic", "title": title, "children": {"attached": []}}
    parent.append(node)
    if children:
        for c in children:
            add_topic(node["children"]["attached"], c)
    return node


