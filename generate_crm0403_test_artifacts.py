def build_excel():
    wb = Workbook()
    ws = wb.active
    ws.title = "测试用例"
    headers = [
        "模块",
        "用例编号",
        "用例标题",
        "优先级",
        "前置条件",
        "测试步骤",
        "预期结果",
        "备注",
    ]
    hf = PatternFill("solid", fgColor="4472C4")
    hfont = Font(color="FFFFFF", bold=True)
    for col, h in enumerate(headers, 1):
        c = ws.cell(row=1, column=col, value=h)
        c.fill = hf
        c.font = hfont
        c.alignment = Alignment(wrap_text=True, vertical="center")

    for i, row in enumerate(CASES, 2):
        for col, v in enumerate(row, 1):
            cell = ws.cell(row=i, column=col, value=v)
            cell.alignment = Alignment(wrap_text=True, vertical="top")

    for col in range(1, len(headers) + 1):
        ws.column_dimensions[get_column_letter(col)].width = 18 if col != 6 else 44
    ws.freeze_panes = "A2"

    ws_meta = wb.create_sheet("需求追溯", 0)
    ws_meta["A1"] = "需求文档"
    ws_meta["B1"] = "CRM0403-客户同步询价单+线索历史记录"
    ws_meta["A2"] = "说明"
    ws_meta["B2"] = (
        "用例依据文档《CRM0403-客户同步询价单+线索历史记录》正文生成；"
        "0410 补充（英文商城登记表编辑再提交同步 CRM）已包含。"
        "交付文件名 CRM0403_CustomerSync_Inquiry_LeadHistory_* 便于跨环境传输，可自行改名。"
    )
    ws_meta.column_dimensions["A"].width = 14
    ws_meta.column_dimensions["B"].width = 70
    ws_meta["B2"].alignment = Alignment(wrap_text=True, vertical="top")

    wb.save(XLSX_PATH)


