def tid():
    return str(uuid.uuid4()).replace("-", "")[:16]


def add_topic(parent_attached, title, children_titles=None):
    node = {
        "id": tid(),
        "class": "topic",
        "title": title,
        "children": {"attached": []},
    }
    parent_attached.append(node)
    if children_titles:
        for child in children_titles:
            add_topic(node["children"]["attached"], child)
    return node


def row_from_case(case):
    cid, module, pri, block, first, scene, pre, steps, expect, note = case
    return (cid, module, pri, block, first, scene, pre, steps, expect, "", note, "")


def style_header(ws, headers, fill_color="4472C4"):
    fill = PatternFill("solid", fgColor=fill_color)
    font = Font(color="FFFFFF", bold=True)
    for col, header in enumerate(headers, 1):
        cell = ws.cell(row=1, column=col, value=header)
        cell.fill = fill
        cell.font = font
        cell.alignment = Alignment(wrap_text=True, vertical="center", horizontal="center")


def write_cases_sheet(ws, rows):
    style_header(ws, HEADERS)
    block_fill = PatternFill("solid", fgColor="FFC7CE")
    first_fill = PatternFill("solid", fgColor="FFF2CC")
    p0_fill = PatternFill("solid", fgColor="DDEBF7")
    for row_idx, row in enumerate(rows, 2):
        for col_idx, value in enumerate(row, 1):
            cell = ws.cell(row=row_idx, column=col_idx, value=value)
            cell.alignment = Alignment(wrap_text=True, vertical="top")
        fill = block_fill if row[3] == "是" else first_fill if row[4] == "是" else p0_fill if row[2] == "P0" else None
        if fill:
            for col_idx in range(1, len(HEADERS) + 1):
                ws.cell(row=row_idx, column=col_idx).fill = fill

    widths = {
        1: 15, 2: 28, 3: 8, 4: 10, 5: 10, 6: 30,
        7: 28, 8: 48, 9: 42, 10: 18, 11: 24, 12: 12,
    }
    for col_idx, width in widths.items():
        ws.column_dimensions[get_column_letter(col_idx)].width = width
    ws.freeze_panes = "A2"
    status_col = get_column_letter(HEADERS.index("用例状态") + 1)
    dv = DataValidation(type="list", formula1=f'"{STATUS_OPTIONS}"', allow_blank=True, showDropDown=False)
